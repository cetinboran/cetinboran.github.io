const setMeta = (lang) => {
  const page = document.body.dataset.page || "home";
  const meta = pageMeta[page]?.[lang] || pageMeta.home[lang];
  document.title = meta.title;
  document.querySelector('meta[name="description"]')?.setAttribute("content", meta.description);
};

export const getThemeLabels = () => {
  const lang = localStorage.getItem("lang") || document.documentElement.lang || "tr";
  const dictionary = translations[lang] || translations.tr;
  return {
    dark: dictionary["theme.dark"],
    light: dictionary["theme.light"]
  };
};

export const applyLanguage = (lang) => {
  const dictionary = translations[lang] || translations.tr;
  document.documentElement.lang = lang;
  setMeta(lang);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (key && dictionary[key]) node.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    const active = button.dataset.langButton === lang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  localStorage.setItem("lang", lang);
};

export const initLanguage = (onChange) => {
  const initialLang = localStorage.getItem("lang") || "tr";
  applyLanguage(initialLang);

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.dataset.langButton);
      onChange?.();
    });
  });
};
